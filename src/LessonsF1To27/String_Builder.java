package LessonsF1To27;

public class String_Builder {
    public static void main(String[] args) {
        System.out.printf("String %.2f \n", 4.523);
        System.out.printf("String %.2f \n", 4.52);
        System.out.printf("String %.2f \n", 4.525131);
        System.out.printf("String %.2f \n", 4.525131431);
        System.out.printf("String %10d \n", 525);
        System.out.printf("String %10d \n", 52);
        System.out.printf("String %10d \n", 5250);
        System.out.printf("String %10d \n", 100000);
        System.out.printf("String %10d \n", 1000000000);
        // % [10] - ширина строки (- чтобы слева) [.3], количество чисел после запятой
    }
}
