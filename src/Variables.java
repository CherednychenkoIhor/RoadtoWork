public class Variables {
    public static void main(String[] args) {
        // Целочисленные
        int myInt = 557;
        short myShort = 3266;
        long myLong = 2452345;
        // Вещественные
        double myDouble = 55.7;
        float myFloat = 32.6f;
        // Characters
        char c = '3';
        // Stores text
        String myString = "Kitty";
        // Logic
        boolean b = true;
        // Primitive data type
        byte myByte = 43; // -128 to 127
        // Out something
        System.out.println(myInt);
    }
}
